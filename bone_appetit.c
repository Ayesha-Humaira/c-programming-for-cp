#include <stdio.h>

int main()
{
    int n, m, x, y;
    scanf("%d%d%d%d", &n, &m, &x, &y);

    int bones = n*x;
    int blood = m*y;

    int total = bones+blood;

    printf("%d", total);

    return 0;
}
